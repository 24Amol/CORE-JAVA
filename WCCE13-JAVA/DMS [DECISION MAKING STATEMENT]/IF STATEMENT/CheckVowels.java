//QNO2=Write A Program To Check Vowels Using If Statement

class CheckVowels
{
	public static void main(String[] args) 
	{	
		char ch='h';
		if(ch=='a'||ch=='e'||ch=='i'||ch=='o'||ch=='u')
		System.out.println(ch+":-Given Character Is Vowel");//true//If It's True Its Executed First Statement
		System.out.println(ch+":-Given Character Is Not Vowel");//false//If It's False Its Executed First Statement
	}
}
/***********************OUTPUT***********************/
//h:-Given Character Is Not Vowel					*
/****************************************************/