class NumEvenOddNumG35 
{

	public static void main(String[] args) 
	{
		int num=24;

		if (num>35)
		{
			if (num%2==0)
			{
				System.out.println(num+ " : This Is Even Number");
			}
			else
			{
				System.out.println(num+ " : This Is Odd Number");
			}
		}
		else
		{
			System.out.println(num+ " : Number Is Less Than 35");
		}
	}
}
