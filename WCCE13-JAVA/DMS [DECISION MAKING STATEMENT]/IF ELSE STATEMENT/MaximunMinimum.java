//QNO=4:-Write A Program To Checks Given Number Is Maximum Or Minimum Using If Else Statement
class MaximunMinimum 
{
	public static void main(String[] args) 
	{	
		int a=11;
		int b=4;
		if(a>b)
		{
			System.out.println(a+":-A Is Greater");
		}
		else
		{
			System.out.println(b+":-B Is Greater");
		}
	}
}
