class EvenNumBet0to25
{
	public static void main(String[] args) 
	{
		int num=0;
		System.out.println("Even Numbers Between 0 To 25:-");
		do
		{
			System.out.println(num);
			num=num+2;
		}
		while (num<=25);
	}
}

