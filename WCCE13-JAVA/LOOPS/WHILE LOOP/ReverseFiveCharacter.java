//QNO3=Write A Program To The First Five Characters In Reverse Orders Using While Loop
class ReverseFiveCharacter 
{
	public static void main(String[] args) 
	{
		char ch='e';

		while (ch>='a')
		{
			System.out.println(ch);
			ch--;
		}
	}
}
