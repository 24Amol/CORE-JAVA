//QNO5=Write A Program To Print The Even Number Between 0 To 25 Using While Loop
class EvenNum0To25 
{
	public static void main(String[] args) 
	{
		int num=2;

		while (num<=25)
		{
			System.out.println(num);
			num=num+2;
		}
	}
}
