//QNO=3:-Write A Program To Print Five Numbers In Reverse Order

class ReverseFirstFiveNum 
{
	public static void main(String[] args) 
	{
		int num;
		System.out.println("The Reverse First Five Characters:-");
		for (num=5;num>=1;num--)
		{
			System.out.println(num);
		}
	}
}
