//QNO=1:-Write A Program To Print First Five Numbers Using For Loop

class PrintFirstFiveNumbers
{
	public static void main(String[] args) 
	{
		int num;
		for (num=1;num<=5;num++ )
		{
			System.out.println(num);
		}
	}
}
