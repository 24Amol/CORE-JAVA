//QNO6:-Design A Method To Calculate The Area Of Rectangle

class MethodCalculateAreaRectangle
{

	public static void calAreaRectangle(int height,int width)
	{
		int area=width*height;
		System.out.println("The Area Of Rectangle Is:-"+area);
	}
	
	public static void main(String[] args) 
	{
			calAreaRectangle(5,10);
	}
}
