//QNO9:-Write A Program to Print ASCII Value Of First Five Numbers
class PrintASCIIValueFirstFiveNum 
{
	public static void main(String[] args) 
	{	
		int n='1';

		for (n='1';n<='5';n++ )
		{
		System.out.println(n);
		}
	}
}
