//QNO10:-Write A Program To Print Fist Five Charaters Into Integer Format

class PrintFirstFiveCharToInt 
{
	public static void main(String[] args) 
	{
		int num='A';

		while(num<='E')
		{
			System.out.println(num);
			num++;
		}
	
	}
}
