//QNO15:-Write A Program TO Print Number 0 To 10
class MethodPrintNumber0To10 
{
	public static void numb(int i,int j)
	{
		while(i<=j)
		{
			System.out.println(i);
			i++;
		}
	
	}
	public static void main(String[] args) 
	{
		numb(0,10);
		//numb(11,20);
	}
}
