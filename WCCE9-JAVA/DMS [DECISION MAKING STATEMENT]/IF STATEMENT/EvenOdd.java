//QNO=3:-Write A Program To Find Even & Odd Number Using If Statement
class EvenOdd 
{
	public static void main(String[] args)
	{	int i=1;
		if (i%2==0)
		System.out.println(i+":-Number Is Even");
		System.out.println(i+":-Number Is Odd");
	}
}

/***********************OUTPUT***********************/
//1:-Number Is Odd									*
