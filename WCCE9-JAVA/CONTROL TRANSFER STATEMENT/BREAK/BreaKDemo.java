//QNO1:-Write A Program For Break Demo To Stop Execution
class BreaKDemo 
{
	public static void main(String[] args) 
	{
		while(true)
		{
			System.out.println("Hi");
			break;//It Stop The Execution And Transfer The Excution To The End Of The Block
		}/*{Control Here}*/
		System.out.println("Hello World");
	}
}
