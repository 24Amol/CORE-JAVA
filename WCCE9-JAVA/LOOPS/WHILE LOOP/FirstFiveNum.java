//QNO=1:-Write A Program To Print The First Five Numbers Using While Loop

class FirstFiveNum 
{
	public static void main(String[] args) 
	{	
		int n=1;//Initialize The Value
		
		while (n<6)//In this We Compare A Value To 6//1<6=true Then Print
		{
			System.out.println(n);
			n++;//Here We Increment The Value of Variable
		}
	}
}
