//QNO=2:-Write A Program To Print First Five Character Using While Loop
class FirstFiveCharacter 
{
	public static void main(String[] args) 
	{	
		char ch='a';

		while (ch < 'f')
		{
			System.out.println(ch);
			ch++;//Here We Increment The Value Of Variable
		}
	}
}
