//QNO=4:-Write A Program To Print Five Characters In Reverse Order

class ReverseFirstFiveCharcaters  
{	
	public static void main(String[] args) 
	{
		char ch;
		System.out.println("The Reverse First Five Characters:-");
		for (ch='e';ch>='a';ch--)
		{
			System.out.println(ch);
		}
	}
}
