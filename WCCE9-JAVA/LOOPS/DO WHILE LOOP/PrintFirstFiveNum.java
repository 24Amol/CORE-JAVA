//QNO=1:-Write A Program to Print First Five Numbers
class PrintFirstFiveNum 
{
	public static void main(String[] args) 
	{
		int n=1;
		System.out.println("The First Five Numbers Are:-");
		do
		{
			System.out.println(n);
			n++;
		}
		while (n<=5);
	}
}
