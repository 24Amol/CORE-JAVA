//QNO=2:Write A Program To calculate The Area Of The Rectangle
class AreaRectangle 
{
	public static void main(String[] args) 
	{
		int width=5;
		int height=10;
		int area=width*height;//Formula To Calculate The Area Of Rectangle
		System.out.println("Area Of Rectangle :- " + area);
	}
}

